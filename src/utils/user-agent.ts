/**
 * Get a random desktop User-Agent string
 * @returns A random User-Agent string for a desktop browser
 */
export function getRandomUserAgent(): string {
  const randomIndex = Math.floor(Math.random() * DESKTOP_USER_AGENTS.length);
  return DESKTOP_USER_AGENTS[randomIndex];
}
